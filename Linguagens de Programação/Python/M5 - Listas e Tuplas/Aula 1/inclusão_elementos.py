# Verificando a inclusão de elementos: Operador (in)

top5 = ["Black Mirror", "Breaking Bad", "Friends", "Game of Thrones", "The Big Bang Theory"]

print("House MD" in top5)
print("Game of Thrones" in top5)
print("friends" in top5)

# esta função serve para indicar se algum intem esta na lista, caso dê "false" o item não esta na lista, caso dê "true", o item esta na lista!
