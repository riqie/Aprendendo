# o comando pop serve como um comando de remove, porém ele vai tirar um elemento de uma >posição< específica:

paises = ["argentina", "brasil", "canada", "dinamarca"]


print(paises), print("2")

print(paises.pop()), print("3")
print(paises), print("4")

print(paises.pop(1)), print("5")
print(paises), print("6")

print(paises), print("7")
