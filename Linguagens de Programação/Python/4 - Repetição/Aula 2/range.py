#o comando range gera uma sequencia em uma lista. veja:
print(list(range(0, 6)))
print(list(range(0, 7)))
print(list(range(0, 8)))
print(list(range(0, -6)))
print(list(range(7, 1)))
print(list(range(9, 5)))
print(list(range(0, 2, 16)))
# perceba que ele não faz o caminho de tra para frente 

print(list(range(4)))
print(list(range(2, 10, 2)))
print(list(range(1, 15, 3)))
# a estrutura é a seguinte: início, fim, passos. Lembre-se: "passos" significa de quanto em quanto os numeros serão gerados.
